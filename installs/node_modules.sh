#!/usr/bin/env bash

source utils.sh

# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

declare -a PACKAGES=(
  'eslint'
  'serve'
  'source-map-explorer'
  'surge'
  'svgo'
  'tern'
  'tldr'
  'fkill-cli'

  'vscode-langservers-extracted'
  'bash-language-server'
  'typescript-language-server'
  'typescript'
)

# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

main() {
  # Check if `npm` is installed
  if ! cmd_exists 'npm'; then
    print_error 'npm/node is required, please install it!\n'
    exit 1
  fi

  # Install the `yarn` packages
  for i in ${PACKAGES[@]}; do
    execute "npm i -g $i" "$i"
  done
}

main
